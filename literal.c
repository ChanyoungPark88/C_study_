#include <stdio.h>

int main()
{
    printf("%d\n", 10);                     //10: integer literal
    printf("%f\n", 0.1f);                   // 0.10000: real number literal
    printf("%c\n", 'a');                    // a: char literal
    printf("%s\n", "Hello, world!");        // Hello, world!: string literal

    return 0;
}